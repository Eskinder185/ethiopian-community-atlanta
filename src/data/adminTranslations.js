export function getAdminTranslation(language, key) {
  const amValue = getNestedValue(adminTranslations.am, key)
  const enValue = getNestedValue(adminTranslations.en, key)

  if (language === 'am' && amValue) return amValue
  if (enValue) return enValue
  return key
}

export function adminT(key, language = 'en') {
  return getAdminTranslation(language, key)
}
